package ok.dht.test.armenakyan.sharding.hashing;

public class NoSuchAlgorithmRuntimeException extends RuntimeException {
    public NoSuchAlgorithmRuntimeException() {
        super();
    }

    public NoSuchAlgorithmRuntimeException(String message) {
        super(message);
    }

    public NoSuchAlgorithmRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoSuchAlgorithmRuntimeException(Throwable cause) {
        super(cause);
    }
}
